package Dayy11;

public class PosNegZeroNo {
	public static void main(String[] args) {
		int n=10;
		if(n==0)
		{
			System.out.println(n+" Number is Zero");
		}
		else if(n>0)
		{
			System.out.println(n+" Number is Positive");
		}
		else
		{
			System.out.println(n+" Number is Negative");
		}
	}
	

}
