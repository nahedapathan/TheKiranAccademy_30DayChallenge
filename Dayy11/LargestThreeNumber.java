package Dayy11;

public class LargestThreeNumber {
	public static void main(String[] args) {
		int a=10;
		int b=5;
		int c=6;
		if(a>b && a>c)
		{
			System.out.println(a+" is largest Number");
		}
		else if(b>a && b>c)
		{
			System.out.println(b+" is largest Number");
		}
		else
		{
			System.out.println(c+" is largest Number");
		}
	}

}
